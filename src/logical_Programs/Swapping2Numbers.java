package logical_Programs;

public class Swapping2Numbers {

	public static void main (String[]args) {
		
		int a=20, b=30;
		
		System.out.println("before swapping nos value is:"+a+ " "+b);

		
		int c = a;
		a=b;
		b=c;
		
		System.out.println("after swapping nos value is:"+a+ " "+b);
		
		
		
	}
}
